            <p class="mt-2">
                In a sentence, this is a melodic prepared piano and sound design instrument. Like
                with all FrozenPlain products, I wanted to try something a little different in terms
                of playing styles and recording techniques, this time in relation to a piano. I
                found that taking the keyboard mechanism off was a good start. This leaves me with
                full access to the strings, which can be played in some interesting ways. This is a
                late Victorian piano, and it certainly has some character, something that I wanted
                to capture. After some experimentation I ended up with a few different playing
                styles: hitting strings with pencils, metal nails, and plucking them with my finger,
                a guitar plectrum, and a handful of other more experimental techniques. The hammered
                styles have an incredible bouncy, slightly detuned, feel to them, like something
                ancient or nordic. And the plucked styles can be soft and harp-like, and quite
                beautiful.
            </p>
            <p class="mt-2">
                This collection of multisampled piano articulations are only half of the story with
                this instrument. It is designed to be a sound design tool to some extent. So
                included are various morphed and synthesised accompaniment sounds, which can be
                layered with any other sounds in the collection. As expected there are the typical
                sound shaping tools for each layer: ADSR, filters, tuning, pan, etc. We are going to
                work on loads of presets for it, and an easy way to browse these. You will be able
                to easily explore this, sometimes dark, sometimes beautiful world of cinematic
                prepared piano. Both pad/ambient patches and ‘keyboard’ patches.
            </p>
            <p class="mt-2">
                This instrument will set the standard for future FrozenPlain sample-based VSTs/AUs:
                compelling and accessible instruments with usability features you’d expect from
                modern software instruments such as resizable and clear GUI, optional tooltips and
                MIDI learning.
            </p>
