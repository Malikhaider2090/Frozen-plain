            <p class="">
                I have been working on audio plugins rather than Kontakt instruments recently. In
                order to get these to the high standard that I want for a FrozenPlain instrument,
                there has been quite a lot of infrastructure development – this is my first
                non-Kontakt sample-based instrument after all. That being said I would like to show
